public class EscapeSequence
{

 public static void main(String [] args)
 { 
     System.out.print("\n");
     System.out.println("Name\tHeight\tGender");
     System.out.println("-----------------------");
     System.out.println("Abc\t5\'4\"\tF"); 
     System.out.println("xyz\t6\'1\"\tM");
     System.out.println("jdk\t5\'7\"\tF");
 }

} 